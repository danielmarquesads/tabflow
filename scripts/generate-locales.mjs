// Other locales: translate key UI strings; fall back by deep-merge with en
function L(map) {
  const out = { ...en };
  for (const [k, v] of Object.entries(map)) {
    out[k] = typeof v === "string" ? { message: v } : v;
  }
  // keep closedN structure
  if (typeof map.closedN === "string") {
    out.closedN = { message: map.closedN, placeholders: { 1: { content: "$1" } } };
  }
  return out;
}

